class BasicDatatypes{
	
	public static void main(String[] args) {
		
		//Exploring Byte Data type
		byte a = 100;
		System.out.println(a);
		
		//Exploring Short Data type
		short b = 10000;
		System.out.println(b);
		
		//Exploring Integer Data type
		int c = 100000;
		System.out.println(c);
		
		//Exploring Long Data type
		long d = 100000000L; 
		System.out.println(d);
		
		//Exploring Float Data type
		float e = 1.564f;
		System.out.println(e);
		
		//Exploring Double Data type
		double f = 104504.4345;
		System.out.println(f);
		
		//Exploring Boolean Datatype
		boolean g = true;
		System.out.println(g);
		
		//Exploring Char Datatype
		char h = 'a';
		System.out.println(h);
		
	}
}